#ifndef _PROGRAM_COUNTER_H_
#define _PROGRAM_COUNTER_H_

#include "ALU.h"
#include "Unit.h"

class ALU;
//////////////////////////////////////////////////////////////////////////////////////////
/// @brief Class for Program Counter
//////////////////////////////////////////////////////////////////////////////////////////
class ProgramCounter : public Unit {

  private:
    std::string instrAddr{"0x00400000"};                   ///< current instruction address

  public:
    //////////////////////////////////////////////////////////////////////////////////////
    /// @brief Constructor
    //////////////////////////////////////////////////////////////////////////////////////
    ProgramCounter() : Unit("Program Counter") {}

    //////////////////////////////////////////////////////////////////////////////////////
    /// @brief Copy Constructor
    //////////////////////////////////////////////////////////////////////////////////////
    ProgramCounter(const ProgramCounter& other) = delete;

    //////////////////////////////////////////////////////////////////////////////////////
    /// @brief Destructor
    //////////////////////////////////////////////////////////////////////////////////////
    ~ProgramCounter() {}

    //////////////////////////////////////////////////////////////////////////////////////
    /// @brief Getter
    //////////////////////////////////////////////////////////////////////////////////////
    std::string getPC() { return instrAddr; }

    //////////////////////////////////////////////////////////////////////////////////////
    /// @brief Setter
    //////////////////////////////////////////////////////////////////////////////////////
    void setPC(std::string& addr) { instrAddr = addr; }

    //////////////////////////////////////////////////////////////////////////////////////
    /// @brief printing function
    //////////////////////////////////////////////////////////////////////////////////////
    virtual void print();

    //////////////////////////////////////////////////////////////////////////////////////
    /// @brief tests the ProgramCounter class
    //////////////////////////////////////////////////////////////////////////////////////
    void testProgramCounter() { }
};

#endif
